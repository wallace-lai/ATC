struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

class Solution {
public:
    ListNode* reverseList(ListNode* head) {
        ListNode dummy(0, nullptr);
        ListNode *tail;

        while (head != nullptr) {
            tail = head->next;
            head->next = dummy.next;
            dummy.next = head;
            head = tail;
        }

        return dummy.next;
    }
};